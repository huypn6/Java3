package com.vn.slide62;

public class Employee {

	//ALT + SHIFT + S + R
	private String txtUname;
	private String txtUpass;
	private String txtDescription;
	private Integer txtAge;

	public String getTxtUname() {
		return txtUname;
	}

	public void setTxtUname(String txtUname) {
		this.txtUname = txtUname;
	}

	public String getTxtUpass() {
		return txtUpass;
	}

	public void setTxtUpass(String txtUpass) {
		this.txtUpass = txtUpass;
	}

	public String getTxtDescription() {
		return txtDescription;
	}

	public void setTxtDescription(String txtDescription) {
		this.txtDescription = txtDescription;
	}

	public Integer getTxtAge() {
		return txtAge;
	}

	public void setTxtAge(Integer txtAge) {
		this.txtAge = txtAge;
	}

}
